<template>
  <div :class="[`scrollbar`, toggleScrollbar]">
    <div
      v-for="item in projects"
      :key="item"
      :style="{ height: heightSegment }"
      class="scrollbar__item"
      :class="[projectHover === item ? 'active' : '']"
    ></div>
  </div>
</template>

<script>
export default {
  name: 'Scrollbar',
  props: {
    projects: {
      type: Number,
      default: 0,
    },
  },
  data: () => {
    return {
      projectHover: 1,
    }
  },
  computed: {
    heightSegment() {
      const totalHeight = 85
      return `${totalHeight / this.projects}vh`
    },
    toggleScrollbar() {
      if (this.$route.path === '/projects') {
        return 'active'
      } else {
        return ''
      }
    },
  },
}
</script>

<style lang="scss">
.scrollbar {
  position: fixed;
  top: vw(70);
  left: $padding-hor;
  width: 5px;
  height: 85vh;

  display: none;
  flex-direction: column;

  &.active {
    display: flex;
  }

  .scrollbar__item {
    background: $color-dark;
    opacity: 0.2;
    &:not(:first-child) {
      margin-top: vw(10);
    }
    &.active {
      opacity: 1;
    }
  }
}
</style>
